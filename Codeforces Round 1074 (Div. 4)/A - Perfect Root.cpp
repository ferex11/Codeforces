#include<bits/stdc++.h>
using namespace std;
#define ll long long
#define endl "\n"
int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;
    cin >> t;
    while (t--) {
        ll n;
        cin >> n;
        for (int i = 1; i <= n; i++) cout << i << " ";
        cout << endl;
    }
    return 0;
}
